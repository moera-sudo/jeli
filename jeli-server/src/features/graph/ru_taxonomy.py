# * Final lookup with folded keys (removes Kazakh letters from the keys themselves too).
RU_TAXONOMY: dict[str, tuple[str, str]] = {_fold(k): v for k, v in _RAW.items()}


def derive_tribe_zhuz(ru: str | None) -> tuple[str, str] | None:
    # * Exact match on the normalized key, then fuzzy lookup (rapidfuzz, threshold > 0.82).
    if not ru:
        return None
    key = _fold(ru)
    if not key:
        return None
    if key in RU_TAXONOMY:
        return RU_TAXONOMY[key]
    match = process.extractOne(key, RU_TAXONOMY.keys(), scorer=fuzz.ratio)
    if match is None:
        return None
    _, score, _ = match
    if score / 100.0 < RU_TAXONOMY_FUZZY_THRESHOLD:
        return None
    return RU_TAXONOMY[match[0]]
